<script>
(function () {
  // === KONFIGURASI SINGKAT ===
  const SHOW_ONLY_DAYS = false; // set true kalau mau tampilkan HANYA "Days"

  function pad(n) { return String(n).padStart(2, '0'); }

  function initCountdown(wrapper) {
    if (!wrapper) return;

    // Ambil target dari data-date
    let ts = Number(wrapper.getAttribute('data-date')) || 0;
    if (!ts) return;

    // Jika sudah dalam detik, ubah ke ms; kalau sudah ms (>= 1e12) biarkan
    if (ts < 1e12) ts = ts * 1000;

    const target = new Date(ts).getTime();

    // Ambil referensi elemen digit (optional)
    const elDays    = wrapper.querySelector('.elementor-countdown-days');
    const elHours   = wrapper.querySelector('.elementor-countdown-hours');
    const elMinutes = wrapper.querySelector('.elementor-countdown-minutes');
    const elSeconds = wrapper.querySelector('.elementor-countdown-seconds');

    // Sembunyikan H/M/S jika hanya mau Days
    if (SHOW_ONLY_DAYS) {
      const hide = (sel) => { const box = wrapper.querySelector(sel)?.closest('.elementor-countdown-item'); if (box) box.style.display = 'none'; };
      hide('.elementor-countdown-hours');
      hide('.elementor-countdown-minutes');
      hide('.elementor-countdown-seconds');
    }

    function tick() {
      const now = Date.now();
      let diff = Math.max(0, target - now); // ms

      // Hitung komponen
      const sec = Math.floor(diff / 1000);
      const days = Math.floor(sec / 86400);
      const hours = Math.floor((sec % 86400) / 3600);
      const minutes = Math.floor((sec % 3600) / 60);
      const seconds = sec % 60;

      // Update UI
      if (elDays)    elDays.textContent    = days;
      if (!SHOW_ONLY_DAYS && elHours)   elHours.textContent   = pad(hours);
      if (!SHOW_ONLY_DAYS && elMinutes) elMinutes.textContent = pad(minutes);
      if (!SHOW_ONLY_DAYS && elSeconds) elSeconds.textContent = pad(seconds);

      // Jika sudah lewat, berhenti di 00
      if (sec <= 0) {
        clearInterval(timer);
        if (elDays)    elDays.textContent    = '0';
        if (!SHOW_ONLY_DAYS && elHours)   elHours.textContent   = '00';
        if (!SHOW_ONLY_DAYS && elMinutes) elMinutes.textContent = '00';
        if (!SHOW_ONLY_DAYS && elSeconds) elSeconds.textContent = '00';
      }
    }

    // Render awal + interval
    tick();
    const timer = setInterval(tick, 1000);
  }

  // Jalankan untuk SEMUA countdown wrapper (kalau ada lebih dari satu)
  function boot() {
    document.querySelectorAll('.elementor-countdown-wrapper').forEach(initCountdown);
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', boot);
  } else {
    boot();
  }
})();
</script>

